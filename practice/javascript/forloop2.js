// A sample *** FOR LOOP *** that will run 45x and says "Hello"
//syntax: for (Initialization; Condition; Iteration) { Code Block}

//3 correct Answers:

//Ans 1:// DEFAULT WAY FOR CONTROLLING A LOOP TO RUN 45X
                     //v counter < 45; must be TRUE for the loop to cont'
for (var counter = 0; counter < 45; counter =counter + 1 ) {
    console.log("Hello");
}

